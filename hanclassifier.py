        outputs, word_atts, sent_atts = han_model(batch_x)

        # Tính toán Loss
        loss = criterion(outputs, batch_y)

        # Backward pass: Tính đạo hàm
        loss.backward()

        # Cập nhật trọng số
        optimizer.step()

        total_train_loss += loss.item()

