def build_vocab(hierarchical_tokens, min_freq=1):
    word_freq = Counter()
    for doc in hierarchical_tokens:
        for sent in doc:
            word_freq.update(sent)

    # Tạo mapping
    word2idx = {'<PAD>': 0, '<UNK>': 1}
    idx2word = {0: '<PAD>', 1: '<UNK>'}

    idx = 2
    for word, freq in word_freq.items():
        if freq >= min_freq:
            word2idx[word] = idx
            idx2word[idx] = word
            idx += 1

    return word2idx, idx2word

